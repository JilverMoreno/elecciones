    private void botonAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonAgregarActionPerformed
        // TODO add your handling code here:
        String numeroCedula = this.campoCedula.getText();
        String nombre = this.campoNombre.getText();
        String correo = this.campoCorreo.getText();
        String telefono = this.campoTelefono.getText();
        String ciudadOrigen = this.comboCiudadOrigen.getSelectedItem().toString();
        String direccion = this.Direccion.getText();
        String partidoPolitico = this.comboPartido.getSelectedItem().toString();
        String mensajeCampania = this.campoMensajeCampania.getText();
        String descripcion = this.campoDescripcion.getText();
        
        /*
        String partido, String mensajeCampania, String descripcion, String numeroCedula, 
        String nombre, String correo, String telefono, String ciudadOrigen, String direccion
        
        
        */
        
         ClsCandidato candidato = new ClsCandidato(partidoPolitico, mensajeCampania, 
                 descripcion,numeroCedula, nombre, correo, telefono, ciudadOrigen, direccion );

        boolean respuesta = this.controladorCandidato.agregarCandidato(candidato);
        
        if(respuesta){
            ObtenerCandidatos();
        }
        
    }//GEN-LAST:event_botonAgregarActionPerformed

